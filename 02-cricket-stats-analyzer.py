# ● Objective: Write a script to analyze cricket stats for a team.
a=int(input("Enter the score of Player 1:"))
b=int(input("Enter the score of Player 2:"))
c=int(input("Enter the score of Player 3:"))
d=int(input("Enter the score of Player 4:"))
